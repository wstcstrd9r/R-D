package com.research.java.dividemodulus;

public class DivideModulus {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		
		divide(a, b);
		mudulus(a,b);
		
		

	}
	
	public static void divide( int a, int b){
		System.out.println(10/b);
	}
	public static void mudulus( int a, int b){
		System.out.println(10%b);
	}

}
